                    <h2>Post-Conquest era</h2>
                    <p>Railroads arrived with the completion of the transcontinental Southern Pacific line from New Orleans to Los Angeles in 1876 and the Santa Fe Railroad in 1885.[55] Petroleum was discovered in the city and surrounding area in 1892, and by 1923, the discoveries had helped California become the country's largest oil producer, accounting for about one-quarter of the world's petroleum output.[56]</p>
                    <p>By 1900, the population had grown to more than 102,000,[57] putting pressure on the city's water supply.[58] The completion of the Los Angeles Aqueduct in 1913, under the supervision of William Mulholland, ensured the continued growth of the city.[59] Because of clauses in the city's charter that prevented the City of Los Angeles from selling or providing water from the aqueduct to any area outside its borders, many adjacent cities and communities felt compelled to join Los Angeles.</p>
                    <p>Los Angeles created the first municipal zoning ordinance in the United States. On September 14, 1908, the Los Angeles City Council promulgated residential and industrial land use zones. The new ordinance established three residential zones of a single type, where industrial uses were prohibited. The proscriptions included barns, lumber yards, and any industrial land use employing machine-powered equipment. These laws were enforced against industrial properties after the fact. These prohibitions were in addition to existing activities that were already regulated as nuisances. These included explosives warehousing, gas works, oil drilling, slaughterhouses, and tanneries. Los Angeles City Council also designated seven industrial zones within the city. However, between 1908 and 1915, the Los Angeles City Council created various exceptions to the broad proscriptions that applied to these three residential zones, and as a consequence, some industrial uses emerged within them. There are two differences between the 1908 Residence District Ordinance and later zoning laws in the United States. First, the 1908 laws did not establish a comprehensive zoning map as the 1916 New York City Zoning Ordinance did. Second, the residential zones did not distinguish types of housing; they treated apartments, hotels, and detached-single-family housing equally.</p>
                    <p>In 1910, Hollywood merged into Los Angeles, with 10 movie companies already operating in the city at the time. By 1921, more than 80 percent of the world's film industry was concentrated in L.A.[64] The money generated by the industry kept the city insulated from much of the economic loss suffered by the rest of the country during the Great Depression.[65] By 1930, the population surpassed one million.[66] In 1932, the city hosted the Summer Olympics.</p>
