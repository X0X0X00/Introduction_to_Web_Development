                <h2>Spanish rule</h2>
                <p>Maritime explorer Juan Rodríguez Cabrillo claimed the area of southern California for the Spanish Empire in 1542, while on an official military exploring expedition, as he was moving northward along the Pacific coast from earlier colonizing bases of New Spain in Central and South America.[45] Gaspar de Portolà and Franciscan missionary Juan Crespí reached the present site of Los Angeles on August 2, 1769.[46]</p>
                <p>In 1771, Franciscan friar Junípero Serra directed the building of the Mission San Gabriel Arcángel, the first mission in the area.[47] On September 4, 1781, a group of 44 settlers known as "Los Pobladores" founded the pueblo (town) they called El Pueblo de Nuestra Señora la Reina de los Ángeles, 'The Town of Our Lady the Queen of the Angels'.[29] The present-day city has the largest Roman Catholic archdiocese in the United States. Two-thirds of the Mexican or (New Spain) settlers were mestizo or mulatto, a mixture of African, indigenous and European ancestry.[48] The settlement remained a small ranch town for decades, but by 1820, the population had increased to about 650 residents.[49] Today, the pueblo is commemorated in the historic district of Los Angeles Pueblo Plaza and Olvera Street, the oldest part of Los Angeles.[50]</p>

