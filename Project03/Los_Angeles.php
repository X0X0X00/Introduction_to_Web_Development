                <h2>Mexican rule</h2>
                <p>New Spain achieved its independence from the Spanish Empire in 1821, and the pueblo now existed within the new Mexican Republic. During Mexican rule, Governor Pío Pico made Los Angeles the regional capital of Alta California.[51] By this time, the new republic introduced more secularization acts within the Los Angeles region.[52] In 1846, during the wider Mexican-American war, marines from the United States occupied the pueblo. This resulted in the siege of Los Angeles where 150 Mexican militias fought the occupiers which eventually surrendered.[53]</p>
                <p>Mexican rule ended during following the American Conquest of California, part of the larger Mexican-American War. Americans took control from the Californios after a series of battles, culminating with the signing of the Treaty of Cahuenga on January 13, 1847.[54] The Mexican Cession was formalized in the Treaty of Guadalupe Hidalgo in 1848, which ceded Los Angeles and the rest of Alta California to the United States.</p>
    
