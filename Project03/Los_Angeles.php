                <h2>Introduction</h2>
                <p>Los Angeles,[a] often referred to by its initials L.A., is the most populous city in the U.S. state of California. With an estimated 3,820,914 residents within the city limits as of 2023,[8] it is the second-most populous city in the United States, behind only New York City; it is also the commercial, financial and cultural center of Southern California. Los Angeles has an ethnically and culturally diverse population, and is the principal city of a metropolitan area of 12.8 million people (2023). Greater Los Angeles, which includes the Los Angeles and Riverside–San Bernardino metropolitan areas, is a sprawling metropolis of over 18.3 million residents.</p>
                <p>The majority of the city proper lies in a basin in Southern California adjacent to the Pacific Ocean in the west and extending partly through the Santa Monica Mountains and north into the San Fernando Valley, with the city bordering the San Gabriel Valley to its east. It covers about 469 square miles (1,210 km2),[6] and is the county seat and most populated city of Los Angeles County, which is the most populous county in the United States with an estimated 9.86 million residents as of 2022.[17] It is the third-most visited city in the U.S. with over 2.7 million visitors as of 2023.[18]</p>
                <p>The area that became Los Angeles was originally inhabited by the indigenous Tongva people and later claimed by Juan Rodríguez Cabrillo for Spain in 1542. The city was founded on September 4, 1781, under Spanish governor Felipe de Neve, on the village of Yaanga.[19] It became a part of the First Mexican Empire in 1821 following the Mexican War of Independence. In 1848, at the end of the Mexican American War, Los Angeles and the rest of California were purchased as part of the Treaty of Guadalupe Hidalgo and became part of the United States. Los Angeles was incorporated as a municipality on April 4, 1850, five months before California achieved statehood. The discovery of oil in the 1890s brought rapid growth to the city.[20] The city was further expanded with the completion of the Los Angeles Aqueduct in 1913, which delivers water from Eastern California.</p>
                <p>Los Angeles has a diverse economy with a broad range of industries. Despite a steep exodus of film and television production since the COVID-19 pandemic,[21] Los Angeles is still one of the largest hubs of American film production,[22][23] the world's largest by revenue; the city is an important site in the history of film. It also has one of the busiest container ports in the Americas.[24][25][26] In 2018, the Los Angeles metropolitan area had a gross metropolitan product of over $1.0 trillion,[27] making it the city with the third-largest GDP in the world, after New York and Tokyo. Los Angeles hosted the Summer Olympics in 1932 and 1984, and will also host in 2028. Despite a business exodus from downtown Los Angeles since the COVID-19 pandemic, the city's urban core is evolving as a cultural center with the world's largest showcase of architecture designed by Frank Gehry.[28]</p>
    
