            <h2>Interpretation of Core Settings</h2>
            <p>The Force is a mysterious and powerful energy field in the Star Wars universe. It permeates the entire galaxy and connects all living things. The Force has a light side and a dark side. The light side represents justice, kindness, and wisdom, while the dark side represents anger, fear, and greed. Jedi Knights use the light side of the Force to maintain peace and justice in the galaxy, while Sith Lords use the dark side to pursue power and domination.</p>
            <p>The lightsaber is an iconic weapon in Star Wars. It is made of plasma, and the hilt can emit a blade of different colors. Blue and green lightsabers are usually wielded by Jedi Knights, while red ones are used by Sith Lords. The lightsaber is not only a weapon for combat but also a symbol of the user's identity and Force ability.</p>
            <p>Hyperspace jump is an important means of interstellar travel. In the Star Wars universe, with the help of a hyperdrive, spaceships can travel at extremely high speeds in hyperspace, covering vast inter - galactic distances. This setting makes communication and trade between planets in the galaxy possible and provides a basis for the interstellar adventures in the story.</p>
        </section>
